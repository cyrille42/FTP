#include "libft.h"

void *ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	char		*d;
	const char	*s;

	s = s2;
	d = s1;
	while (n--)
	{
		if (*s == c)
		{
			*d++ = *s++;
			return (d);
		}
		*d++ = *s++;
	}
	return (NULL);
}
